void	draw_map2D(void)
{
	void *mlx;
	t_game *img;
	int x;
	int y;
	int isone = 1;
	int mapx = 0;
	int mapy = 0;

	y = 0;
	img = (t_game *)malloc(sizeof(t_game));
	mlx = mlx_init(WINDOW_WIDTH, WINDOW_HEIGHT, "THIS IS CUBE3D", 100);
	img->img = mlx_new_image(mlx, WINDOW_WIDTH, WINDOW_HEIGHT);
	mlx_image_to_window(mlx, img->img, 0, 0);
    y = 0;
    while (y < WINDOW_HEIGHT)
    {
        x = 0;
        while (x < WINDOW_WIDTH)
        {
            mapx = x * MAP_WIDTH / WINDOW_WIDTH;
            mapy = y * MAP_HEIGHT / WINDOW_HEIGHT;
            isone = world_map[mapx][mapy];
            if (isone == 1)
                mlx_put_pixel(img->img, x, y, 0xFFFFFFFF);
            else
                mlx_put_pixel(img->img, x, y, 0x00000000);
            x++;
        }
        y++;
    }
		mlx_loop(mlx);
	}

	int main(void)
	{
		draw_map2D();
		return (0);
	}
